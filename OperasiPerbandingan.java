public class OperasiPerbandingan {
    public static void main(String[] args) {

        int nilai = 100;
        int nilai1 = 100;

        // lebih dari >
        System.out.println(nilai > nilai1);
        // kurang dari <
        System.out.println(nilai < nilai1);
        // lebih dari sama dengan >=
        System.out.println(nilai >= nilai1);
        // kurang dari sama dengan <=
        System.out.println(nilai <= nilai1);
        // sama dengan ==
        System.out.println(nilai == nilai1);
        // tidak sama dengan !=
        System.out.println(nilai != nilai1);


    }
}
